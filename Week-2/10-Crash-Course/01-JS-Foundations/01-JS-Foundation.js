/**
 * Interpreted Vs Compiler :
 * 
 * 1. What is a compiler?
 *    - Compilers convert high-level developer-friendly code into 0s and 1s.
 *    - Examples of compiled languages are C, C++, Golang, Rust etc.
 *    - When you compile the code which is written in C++, first it goes through the 
 *      code and produces the output file, later it runs the program only if the code
 *      seems to be correct.
 * 
 * 2. What is Interpretor?
 *   - Interpretors run through a program line by line and execute each command.
 *   - Examples of Interpreted languages are PHP, Ruby, Python and javascript.
 *   - Usually (interpreted languages) go line by line. It can run partially if
 *     the error occurs in the later stage of code.
*/

/**
 * Q. Where to run JavaScript Code?
 * => 1. In Source Code Editor(Visual Studio) - Create a file name with "js" extension.
 *       Write the code inside it and open the terminal and write the command
 *       "node file_name.js" to run the code.
 * => 2. In repl.it - It let's you compile code without having it locally on your
 *       machine. Sign in to repl.it, click on create Repl and choose the runtime
 *       environment i.e. Node to run js file.
*/

