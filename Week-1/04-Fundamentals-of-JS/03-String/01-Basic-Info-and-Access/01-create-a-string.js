/**
 * Introduction: Strings 
 * > String = Sentence ya word 
 *   - Har character ka apna position hota hai (index)
 *   - Length batata hai kitne characters hain
 *   - Different tarike se access kar sakte hain
*/

/**
 * Basic Concept:
 * > String character ka sequence hota hai. 
 * > JavaScript mein strings immutable hote hain (change nahi ho sakte).
*/

const name = "JavaScript";
console.log(name.length);    // 10 characters
console.log(name[0]);        // 'J'
console.log(name.charAt(0)); // 'J'