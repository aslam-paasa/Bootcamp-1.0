/**
 * Arrow Function:
 * - Also known as
 *   a. Short Arrow Function
 *   b. Fat Arrow Function
 *   c. Lambda Arrow Function
*/

/**
 * Challenge:
 * Can you convert the below arrow function into function declaration
 * (normal function) format?
*/

/**
 * 1. With no parameter:
*/
const getNum = () => 2;

/**
 * 2. With one parameter:
*/
const isOne = num => num == 1;


/**
 * Function Call:
*/
console.log(getNum());
console.log(isOne(12));