/**
 * Events vs Listener:
 * - Event is a thing that happens in the browser like click, mouseover, etc.
 * - Listener is a function that will be called when the event happens.
*/

/**
 * Capturing vs Bubbling:
 * - Capturing is a way to handle events from the top to the bottom of the DOM.
 * - Bubbling is a way to handle events from the bottom to the top of the DOM.
 *   (We generally use bubbling with stopPropagation())
*/